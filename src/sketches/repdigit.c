/* Во время разработки некоторой задачи Саша решил сгенерировать несколько новых
тестов. Каждый тест Саши должен представлять собой натуральное число, не меньшее
l и не большее r. Кроме того, натуральное число в тесте обязательно должно
состоять из одинаковых цифр.

Например, число 999 подходит под это требование, а число 123 — нет. Какое
максимальное число различных тестов сможет создать Саша?

Формат входных данных: В единственной строке вводятся два натуральных числа l,r
ограничения на тесты Саши. Обратите внимания, что эти числа не вместятся в
32-битный тип данных, используйте 64-битный при необходимости

Формат выходных данных: Выведите одно число — количество тестов, которое может
сделать Саша.

Замечание
В первом тесте Саше подходят номера [4,5,6,7 ].
Во втором тесте подходят все числа, кратные 11, от 11 до 99.

Примеры данных
Пример 1         Пример  2
Вход:  4 7       Вход:   10 100
Выход: 4         Выход:  9
*/

#include <stdio.h>

int main(void) {
  long long l = 0, r = 0;  // c99/c11 guarantees 64-bit for lli
  if (scanf("%lld %lld", &l, &r) != 2) return -1;

  int count = 0;

  for (int len = 1; len <= 19; len++) {
    long long min_same = 0, max_same = 0;

    for (int k = 0; k < len; k++) {
      min_same = min_same * 10 + 1;
      max_same = max_same * 10 + 9;
    }

    if (max_same >= l && min_same <= r) {
      for (int digit = 1; digit <= 9; digit++) {
        long long num = 0;
        for (int k = 0; k < len; k++) num = num * 10 + digit;
        if (num >= l && num <= r) count++;
      }
    }
  }

  printf("%d", count);
  return 0;
}
